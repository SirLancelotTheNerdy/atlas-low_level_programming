#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtracts 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiple 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds the mod of 2 integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
