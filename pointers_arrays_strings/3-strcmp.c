/**
 *_strcmp - compares two strings.
 *@s1: pointer to first string.
 *@s2: pointer to second string.
 *.
 */
int _strcmp(char *s1, char *s2)
{
	int counter, cmpvl;

	counter = 0;
	while (s1[counter] == s2[counter] && s1[counter] != '\0')
	{
		counter++;
	}

	cmpvl = s1[counter] - s2[counter];
	return (cmpvl);
}
